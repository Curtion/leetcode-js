/**
 * Definition for a binary tree node.
 * function TreeNode(val) {
 *     this.val = val;
 *     this.left = this.right = null;
 * }
 */
/**
 * @param {TreeNode} root
 * @return {TreeNode}
 */
var invertTree = function (root) {
  let tree = (root) => {
    if (root === null) {
      return root
    }
    let left = tree(root.left)
    let right = tree(root.right)
    let newTree = new TreeNode(root.val)
    newTree.left = right
    newTree.right = left
    return newTree
  }
  return tree(root)
};
